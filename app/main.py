def load_and_prep_image(img_path, img_shape=224, scale=True):
    """
    Reads in an image from img_path, turns it into a tensor and reshapes into
    (224, 224, 3).

    Parameters
    ----------
    img_path (str): string filename of target image
    img_shape (int): size to resize target image to, default 224
    scale (bool): whether to scale pixel values to range(0, 1), default True
    """
    # Read in the image
    img = tf.io.read_file(img_path)
    # Decode it into a tensor
    img = tf.io.decode_image(img)
    # Resize the image
    img = tf.image.resize(img, [img_shape, img_shape])
    if scale:
        # Rescale the image (get all values between 0 and 1)
        return img/255.
    else:
        return img


def model_predict(img_path, model):
    img = load_and_prep_image(img_path, scale=False)

    pred_prob = model.predict(tf.expand_dims(img, axis=0))
    pred_class = class_names[pred_prob.argmax()]

    return pred_class + f" with probability {tf.reduce_max(pred_prob):.2f}"


